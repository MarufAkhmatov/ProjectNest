const dicts: Record<Lang, Dict> = { en, ru, uz };

export const LANGS: { code: Lang; label: string }[] = [
  { code: "en", label: "EN" },
  { code: "ru", label: "RU" },
  { code: "uz", label: "UZ" },
];

type Vars = Record<string, string | number>;
type Ctx = {
  lang: Lang; setLang: (l: Lang) => void;
  t: (k: string) => string;
  tf: (k: string, vars?: Vars) => string;
  d: Dict;
};

const I18nCtx = createContext<Ctx>({ lang: "en", setLang: () => {}, t: (k) => k, tf: (k) => k, d: en });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>("en");
  // Temur can switch the UI language ("ruschaga o't" / "switch to English")
  useEffect(() => {
    const h = (e: Event) => {
      const l = (e as CustomEvent).detail?.lang;
      if (l === "en" || l === "ru" || l === "uz") setLang(l);
    };
    window.addEventListener("pn-lang", h);
    return () => window.removeEventListener("pn-lang", h);
  }, []);
  const d = dicts[lang];
  const t = (k: string) => ((d as any)[k] ?? (en as any)[k] ?? k) as string;
  const tf = (k: string, vars: Vars = {}) => {
    let s = t(k);
    for (const [vk, vv] of Object.entries(vars)) s = s.split(`{${vk}}`).join(String(vv));
    return s;
  };
  return <I18nCtx.Provider value={{ lang, setLang, t, tf, d }}>{children}</I18nCtx.Provider>;
}

export const useI18n = () => useContext(I18nCtx);
